package tech.tora.quaver.types;

public class Cell {

	public String type;
	public String language;
	public String data;
	
	public Cell() {
		// Do Nothing
	}
	
	public Cell(String type, String language, String data) {
		this.type = type;
		this.language = language;
		this.data = data;
	}
	
	
}
